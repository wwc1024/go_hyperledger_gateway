package iot_device

import (
	"bytes"
	"fmt"
	"os/exec"
)

func AppStart() {
	cmd := exec.Command("/bin/bash", "./test2.sh")
	var out bytes.Buffer
	var stderr bytes.Buffer
	cmd.Stdout = &out
	cmd.Stderr = &stderr
	err := cmd.Run()
	if err != nil {
		fmt.Println(fmt.Sprint(err) + ": " + stderr.String())
		return
	}
	fmt.Println("Result: " + out.String())
}

